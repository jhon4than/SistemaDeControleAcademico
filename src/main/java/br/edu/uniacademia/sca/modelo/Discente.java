package br.edu.uniacademia.sca.modelo;

import java.io.Serializable;

/**
 *
 * @author jhon4
 */
public class Discente implements Serializable {
    private String cpf;
    private String rg;
    private String nome;
    private String dataNasc;
    private String sexo;
    private String cep;
    private String logradouro;
    private String numero;
    private String complemento;
    private String bairro;
    private String cidade;
    private String estado;
    private String situacao;
    private String matricula;

    public Discente() {
    }

    public Discente(String cpf, String rg, String nome, String dataNasc, String sexo, String cep, String logradouro, String numero, String complemento, String bairro, String cidade, String estado, String situacao, String matricula) {
        this.cpf = cpf;
        this.rg = rg;
        this.nome = nome;
        this.dataNasc = dataNasc;
        this.sexo = sexo;
        this.cep = cep;
        this.logradouro = logradouro;
        this.numero = numero;
        this.complemento = complemento;
        this.bairro = bairro;
        this.cidade = cidade;
        this.estado = estado;
        this.situacao = situacao;
        this.matricula = matricula;
        
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDataNasc() {
        return dataNasc;
    }

    public void setDataNasc(String dataNasc) {
        this.dataNasc = dataNasc;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public String getLogradouro() {
        return logradouro;
    }

    public void setLogradouro(String logradouro) {
        this.logradouro = logradouro;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getComplemento() {
        return complemento;
    }

    public void setComplemento(String complemento) {
        this.complemento = complemento;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getSituacao() {
        return situacao;
    }

    public void setSituacao(String situacao) {
        this.situacao = situacao;
    }

    public Object dadosAlun() {
        Object alun[] = {matricula,cpf, rg, nome, dataNasc, sexo, cep, logradouro, numero, complemento, bairro, cidade, estado, situacao};
        return alun;
    }
    
    public Object dadosAlunNome() {
        Object alunNome[] = {matricula, nome,situacao};
        return alunNome;
    }
    
    
    
}
