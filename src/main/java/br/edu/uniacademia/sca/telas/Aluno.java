    private void jbtRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtRemoverActionPerformed

        DefaultTableModel modelo = (DefaultTableModel) jtADados.getModel();
        List<Discente> list = new ArrayList<Discente>();
        ArrayList<Discente> nova_list = new ArrayList<Discente>();
        linhab = jtADados.getSelectedRow();
        Object nome = modelo.getValueAt(linhab, 0);
        list = arqIn.getTodos();

        for (int i = 0; i < list.size(); i++) {
            if (!(list.get(i).getCpf().equals(txtCPFALUNO.getText()))) {
                nova_list.add(list.get(i));
            }
        }
        try {
            arqOut.recadastrar_todos(nova_list);
        } catch (IOException ex) {
            Logger.getLogger(Aluno.class.getName()).log(Level.SEVERE, null, ex);
        }
        Limpar();
        modelo.removeRow(linhab);
        txtCPFALUNO.setEnabled(true);
        JOptionPane.showMessageDialog(null, "Registro apagado com sucesso");

    }//GEN-LAST:event_jbtRemoverActionPerformed

