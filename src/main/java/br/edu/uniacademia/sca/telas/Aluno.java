    private void jbAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAdicionarActionPerformed

        if (txtMatricula.getText().isEmpty() ||txtBairro.getText().isEmpty() || txtCEP.getText().isEmpty() || txtCPFALUNO.getText().isEmpty() || txtCidade.getText().isEmpty()
                || txtDataNasc.getText().isEmpty() || txtLogradouro.getText().isEmpty() || txtNome.getText().isEmpty() || txtRGALUNO.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe os campos marcados", "Informação ausente", JOptionPane.WARNING_MESSAGE);
        } else {
            Discente disc = new Discente();

            disc.setMatricula(txtMatricula.getText());
            disc.setCpf(txtCPFALUNO.getText());
            disc.setRg(txtRGALUNO.getText());
            disc.setBairro(txtBairro.getText());
            disc.setCep(txtCEP.getText());
            disc.setCidade(txtCidade.getText());
            disc.setComplemento(txtComplemento.getText());
            disc.setDataNasc(txtDataNasc.getText());
            disc.setEstado(jcbEstado.getSelectedItem().toString());
            disc.setLogradouro(txtLogradouro.getText());
            disc.setNome(txtNome.getText());
            disc.setNumero(txtNumero.getText());
            if (jrbSexoM.isSelected()) {
                disc.setSexo("Masculino");
            } else if (jrbSexoF.isSelected()) {
                disc.setSexo("Feminino");
            } else {
                JOptionPane.showMessageDialog(null, "Informe o sexo do Aluno(a).", "Informação ausente", JOptionPane.WARNING_MESSAGE);
            }
            if (jckbEstatus.isSelected()) {
                disc.setSituacao("Ativo");
            } else {
                disc.setSituacao("Inativo");
            }

            DefaultTableModel modelo = (DefaultTableModel) jtADados.getModel();

            try {
                arqOut.Adicionar(disc);
            } catch (IOException ex) {
                Logger.getLogger(Aluno.class.getName()).log(Level.SEVERE, null, ex);
            }

            modelo.addRow((Object[]) disc.dadosAlun());

            Limpar();
        }
    }//GEN-LAST:event_jbAdicionarActionPerformed

